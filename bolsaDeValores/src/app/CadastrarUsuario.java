    private void tratarCadastro(){
        if( verificarErros() ){
                showErroPreenchimento();
                return;
            }
            int codp, idade;
            try{
                codp = Integer.parseInt( textFieldCod.getText() );
                idade = Integer.parseInt( textFieldIdade.getText() );
            } catch ( NumberFormatException ex ){
                showErroPreenchimento();
                return;
            }
            
            Paciente paciente = new Paciente(codp, textFieldNome.getText(), textFieldCPF.getText(), idade, textFieldCidade.getText());
            boolean resultado = Main.getBD().cadastrarPaciente( paciente );
            if( resultado == false ){
                showErroBD();
                return;
            }
            int opcao = JOptionPane.showConfirmDialog(this, "Paciente adicionado com sucesso.\n"
                    + "Deseja adicionar outro registro?", "Operação Realizada com Sucesso", JOptionPane.YES_NO_OPTION);
            if( opcao == JOptionPane.YES_OPTION ){
                limpaCampos();
            }
            else{
                Main.showBDV();
                this.dispose();
            }
    }
    
    
    
    private void tratarAlteracao(){
        if( textFieldCod.isEnabled() ){
            int codigo;
            try{
                codigo = Integer.parseInt( textFieldCod.getText() );
            } catch ( NumberFormatException ex ){
                showErroPreenchimento();
                return;
            }

            Paciente paciente = Main.getBD().getPaciente( codigo );
            if( paciente==null ){
                JOptionPane.showMessageDialog(this, "Nenhum paciente foi encontrado com este código!", "Resultado", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                textFieldCod.setText( ""+paciente.codp );
                textFieldNome.setText( paciente.nome );
                textFieldCPF.setText( paciente.cpf );
                textFieldIdade.setText( ""+paciente.idade );
                textFieldCidade.setText( paciente.cidade );
                alterarEdicaoCampos( true );
                textFieldCod.setEnabled( false );
                botaoAcao.setText("Alterar");
            }
        }

        else{ // realizar alteração na tabela
            if( verificarErros() ){
                showErroPreenchimento();
                return;
            }
            int codp, idade;
            try{
                codp = Integer.parseInt( textFieldCod.getText() );
                idade = Integer.parseInt( textFieldIdade.getText() );
            } catch ( NumberFormatException ex ){
                showErroPreenchimento();
                return;
            }
            Paciente paciente = new Paciente(codp, textFieldNome.getText(), textFieldCPF.getText(), idade, textFieldCidade.getText());
            boolean resultado = Main.getBD().alterarPaciente(paciente);
            if( resultado == false ){
                showErroBD();
                return;
            }
            int opcao = JOptionPane.showConfirmDialog(this, "Dados alterados com sucesso.\n"
                    + "Deseja alterar outro registro?", "Operação Realizada com Sucesso", JOptionPane.YES_NO_OPTION);
            if( opcao == JOptionPane.YES_OPTION ){
                limpaCampos();
                alterarEdicaoCampos( false );
                textFieldCod.setEnabled( true );
                botaoAcao.setText("Consultar");
            }
            else{
                Main.showMainWindow();
                this.dispose();
            }
        }
    }
    
    
    
    
    
    private boolean verificarErros(){
        if( textFieldNome.getText().length()<=3 && textFieldCPF.getText().length()<=3 ){
            showErroPreenchimento();
            return true;
        }
        return false;
    }
    
    
    
    private void showErroPreenchimento(){
        JOptionPane.showMessageDialog(this, "Verifique o preenchimento dos dados!", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    private void showErroBD(){
        JOptionPane.showMessageDialog(this, "Ocorreu algum erro ao registrar dados no BD!", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    
    
    
    
    private void limpaCampos(){
        textFieldCod.setText("");
        textFieldNome.setText("");
        textFieldCPF.setText("");
        textFieldIdade.setText("");
        textFieldCidade.setText("");
    }
    
    
    
    private void alterarEdicaoCampos(boolean opcao){
        textFieldCod.setEnabled( opcao );
        textFieldNome.setEnabled( opcao );
        textFieldCPF.setEnabled( opcao );
        textFieldIdade.setEnabled( opcao );
        textFieldCidade.setEnabled( opcao );
    }
    
    
    
    
